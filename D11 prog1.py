#WAP to perform operations on string
s='hello world'
#To extract some part of the string we have to use slicing operation
print(s[1:]) #extract the string elements starting with index1 and up to last index value
print(s[::])
print(s[:5])
print(s[6:11])
print(s[6:])
print(s[1:9:2]) #starting from 1 up to 8
print(s[::-1])
print(s[10:0:-1])