#Example on Modulo division operator %
s=5%2 #It always return remainder value
print('This is from string modulo',s)
print(type(s))
n=5/2 #It always returns quotient value in the form of float
print('This is from normal division',n)
print(type(n))
z=5//2 #It returns quotient based on the mode
print('This is from Floor division',z)
print(type(z))
e=2**3 #Exponent operator
